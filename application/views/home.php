<h4><b>Fonctionnement</b></h4>
<p>Le principe de la pyramide holographique est basé sur la réflexion de l’image dans une pyramide en verre. L’effet produit est en fait un faux d’hologramme. En effet, il est impossible de tourner autour d’un personnage produit par cet hologramme dans le sens où l’image renvoyé à l’œil sera toujours la même, quel que soit la position autour de la pyramide. Cependant, l’illusion fonctionne puisque on ne distingue toujours qu’un seul côté à la fois.</p>

