<h4><b>Fabriquer un modèle pour un hologramme réaliste</b></h4>
<p>Le choix des dimensions de la pyramide influence grandement le rendu. Il faut choisir une base qui correspond aux dimensions de l’écran. Le seul paramètre affectant la direction du reflet est la hauteur de la pyramide. Pour un écran de 15 pouces, les dimensions idéales d’une face de la pyramide sont de 30x25x25. L’idéal est que l'hologramme soit visible au centre de la pyramide.</p>
<br>
<h4><b>Conclusion</b></h4>
Il est possible de projeter tous types d’images et même de placer un objet à l’intérieur et d’interagir avec en utilisant des images adaptées. De nombreuses entreprises ont commencé à utiliser ce type d’installation pour faire leur publicité, ou même lors de conférences pour présenter des modèles 3D. La physique en rapport avec cette expérience est simple à maitriser et il est possible de remplacer la partie en verre par un trièdre, ou bien sûr par une simple plaque de verre.</p>
